class ForloopExample{
      public static void main(String[] args) {
        for(int i=1;i<=5;i++) {
            System.out.println(i);
        }
     }
}

class ForloopExample2{
    public static void main(String[] args) {
        for(int i=2;i<=50;i=i+2)
            System.out.println(i);
    }
}


class ForloopExample3{
    public static void main(String[] args) {
        int sum=0;
        for(int i=0;i<=10;i++) {
            sum=sum+i;
            System.out.println(sum);
        }  
    }
}


